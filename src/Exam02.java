public class Exam02 {
    public static void main(String args[]){
        int var1 = 10;
        float var2 = 10.1f;  // float형의 경우 f가 접미사로 붙음
        double var3 = 10.2;
        char var4 = '안';  // 단일문자는 따옴표
        String var5 = "안드로이드";  // 문자열은 큰따옴표.. 아 파이썬 하다가 다시 하니까 난리난다..
        // 위 4개는 기본적인 자료형이고 String의 경우 클래스로 자료형 기능을 구현함
        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);
        System.out.println(var4);
        System.out.println(var5);
    }
}
